package mojo.km.security;

public class UsergroupRoleEntityBean
{

    private int groupid;
    private int roleid;

    /** @return the groupid */
    public int getGroupid()
    {
	return groupid;
    }

    /** @param groupid
     *            the groupid to set */
    public void setGroupid(int groupid)
    {
	this.groupid = groupid;
    }

    /** @return the roleid */
    public int getRoleid()
    {
	return roleid;
    }

    /** @param roleid
     *            the roleid to set */
    public void setRoleid(int roleid)
    {
	this.roleid = roleid;
    }

}
