package mojo.km.security;

public class UserRoleEntityBean
{
    private int userid;
    private int roleid;

    /** @return the userid */
    public int getUserid()
    {
	return userid;
    }

    /** @param userid
     *            the userid to set */
    public void setUserid(int userid)
    {
	this.userid = userid;
    }

    /** @return the roleid */
    public int getRoleid()
    {
	return roleid;
    }

    /** @param roleid
     *            the roleid to set */
    public void setRoleid(int roleid)
    {
	this.roleid = roleid;
    }

}
