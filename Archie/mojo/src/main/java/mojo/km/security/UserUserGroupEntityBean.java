package mojo.km.security;

public class UserUserGroupEntityBean
{
    private int groupid;
    private int userid;

    /** @return the groupid */
    public int getGroupid()
    {
	return groupid;
    }

    /** @param groupid
     *            the groupid to set */
    public void setGroupid(int groupid)
    {
	this.groupid = groupid;
    }

    /** @return the userid */
    public int getUserid()
    {
	return userid;
    }

    /** @param userid
     *            the userid to set */
    public void setUserid(int userid)
    {
	this.userid = userid;
    }

}
