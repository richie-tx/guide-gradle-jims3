package ui.juvenilecase.interviewinfo.form;
import java.util.*;
public class ProgramService
{
    private String program;
    private String service;
    private String selectedProgramService;
    private String beginDate;
    private String endDate;
    
    public String getProgram()
    {
        return program;
    }
    public void setProgram(String program)
    {
        this.program = program;
    }
    public String getService()
    {
        return service;
    }
    public void setService(String service)
    {
        this.service = service;
    }
    public String getSelectedProgramService()
    {
        return selectedProgramService;
    }
    public void setSelectedProgramService(String selectedProgramService)
    {
        this.selectedProgramService = selectedProgramService;
    }
    public String getBeginDate()
    {
        return beginDate;
    }
    public void setBeginDate(String beginDate)
    {
        this.beginDate = beginDate;
    }
    public String getEndDate()
    {
        return endDate;
    }
    public void setEndDate(String endDate)
    {
        this.endDate = endDate;
    }
    
    
}
