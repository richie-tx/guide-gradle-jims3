package ui.juvenilecase.action;

public class JuvenileRecord {
    private String juvenileName;
    private String juvenileNumber;
    private String facilityLocation;
    private String facilityStatus;
    private String supervisionNumber;
    private String supervisionType;
    private String referral;
    private String offense;
    private String expectedEndDate;
    private String courtDate;
    private String courtID;
    private String pendingAllegation;
    

    // Getter methods
    public String getJuvenileName() {
        return juvenileName;
    }

    public String getJuvenileNumber() {
        return juvenileNumber;
    }

    public String getFacilityLocation() {
        return facilityLocation;
    }

    public String getFacilityStatus() {
        return facilityStatus;
    }

    public String getSupervisionNumber() {
        return supervisionNumber;
    }

    public String getSupervisionType() {
        return supervisionType;
    }

    public String getReferral() {
        return referral;
    }

    public String getOffense() {
        return offense;
    }

    public String getExpectedEndDate() {
        return expectedEndDate;
    }

    public String getCourtDate() {
        return courtDate;
    }

    public String getCourtID() {
        return courtID;
    }

    public String getPendingAllegation() {
        return pendingAllegation;
    }

    // Setter methods
    public void setJuvenileName(String juvenileName) {
        this.juvenileName = juvenileName;
    }

    public void setJuvenileNumber(String juvenileNumber) {
        this.juvenileNumber = juvenileNumber;
    }

    public void setFacilityLocation(String facilityLocation) {
        this.facilityLocation = facilityLocation;
    }

    public void setFacilityStatus(String facilityStatus) {
        this.facilityStatus = facilityStatus;
    }

    public void setSupervisionNumber(String supervisionNumber) {
        this.supervisionNumber = supervisionNumber;
    }

    public void setSupervisionType(String supervisionType) {
        this.supervisionType = supervisionType;
    }

    public void setReferral(String referral) {
        this.referral = referral;
    }

    public void setOffense(String offense) {
        this.offense = offense;
    }

    public void setExpectedEndDate(String expectedEndDate) {
        this.expectedEndDate = expectedEndDate;
    }

    public void setCourtDate(String courtDate) {
        this.courtDate = courtDate;
    }

    public void setCourtID(String courtID) {
        this.courtID = courtID;
    }

    public void setPendingAllegation(String pendingAllegation) {
        this.pendingAllegation = pendingAllegation;
    }
    
}
