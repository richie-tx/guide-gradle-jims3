package ui.supervision.supervisionorder;

public class SubConditionBean 
{
	private String subConditionNumber = "";
	private String subConditionName = "";
	
	
	/**
	 * @return the subConditionNumber
	 */
	public String getSubConditionNumber() {
		return subConditionNumber;
	}
	/**
	 * @param subConditionNumber the subConditionNumber to set
	 */
	public void setSubConditionNumber(String subConditionNumber) {
		this.subConditionNumber = subConditionNumber;
	}
	/**
	 * @return the subConditionName
	 */
	public String getSubConditionName() {
		return subConditionName;
	}
	/**
	 * @param subConditionName the subConditionName to set
	 */
	public void setSubConditionName(String subConditionName) {
		this.subConditionName = subConditionName;
	}
}
